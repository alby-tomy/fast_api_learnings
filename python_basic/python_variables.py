# A variable stores data.
# Think of it like a labeled box that holds a value.
print("\n=== 3. VARIABLES ===")
name = "Alice"          # storing text
age = 16                # storing an integer
height = 5.4            # storing a decimal number
is_student = True       # storing a boolean value

print("Name:", name)
print("Age:", age)
print("Height:", height)
print("Is student:", is_student)