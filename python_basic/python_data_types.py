# Python has many data types.
# Here are some common beginner-friendly ones.
print("\n=== 4. BASIC DATA TYPES ===")
message = "Welcome"     # str -> string (text)
count = 10              # int -> integer (whole number)
price = 99.99           # float -> decimal number
passed = False          # bool -> True or False

print("Value:", message, "| Type:", type(message))
print("Value:", count, "| Type:", type(count))
print("Value:", price, "| Type:", type(price))
print("Value:", passed, "| Type:", type(passed))